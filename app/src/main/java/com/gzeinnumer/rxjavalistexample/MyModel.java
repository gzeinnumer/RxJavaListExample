package com.gzeinnumer.rxjavalistexample;

public class MyModel {
    int index;
    String data;

    public MyModel(int index, String data) {
        this.index = index;
        this.data = data;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
